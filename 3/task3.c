# include <stdio.h>

// Ввести два числа и найти их разность
// Нужно напечатать разность двух чисел
// Данные на входе:       Два целых числа через пробел
// Данные на выходе:    Одно целое число


int main(void)
{
   int a, b;
   scanf("%d%d", &a, &b);
   printf("%d - %d = %d\n", a, b, (a-b));

  
}